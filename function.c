#include<stdio.h>
#include <stdlib.h>
#include<math.h>

int main(void)
{
/*mathematical expressions*/
	/*power function*/
	printf("This is how to use pow: 5^ 4 = %lf\n", pow(5, 7));
	/*squareroot function*/
	printf("This is how to find squareroot of numbers: 196 = %lf\n", sqrt(196));
	/*ceil function*/
	printf("This ceil function rounds up a value to uppermost value: 56.497 = %lf\n", ceil(56.497));
	/*floor function*/
	printf("This floor fuction rounds up a number to lower value: 78.98 = %lf\n", floor(78.98));
	return (0);
}
